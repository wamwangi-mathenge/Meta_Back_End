# Python While Loops

favorites = ['Creme Brulee', 'Apple Pie', 'Churros', 'Tiramiso', 'Chocolate Cake']

# As in the for loop, you need to specify a condition to make the loop over n times depending on the count itself

# Declare a counter
count = 0

while count < len(favorites):
    print(favorites[count])
    count += 1